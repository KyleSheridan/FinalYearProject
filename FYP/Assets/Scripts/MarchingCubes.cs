    public CubeGrid cubeGrid;

    public bool faceNormalsOnOutside = false;

    List<Vector3> vertices;
    List<int> triangles;

    public void GenerateMesh(int[,,] map, float cubeSize)
    {
        cubeGrid = new CubeGrid(map, cubeSize);

        vertices = new List<Vector3>();
        triangles = new List<int>();

        for (int x = 0; x < cubeGrid.cubes.GetLength(0); x++)
        {
            for (int y = 0; y < cubeGrid.cubes.GetLength(1); y++)
            {
                for (int z = 0; z < cubeGrid.cubes.GetLength(2); z++)
                {
                    TriangulateCube(cubeGrid.cubes[x, y, z]);
                }
            }
        }

        DrawTriangles();
    }

    public void DrawTriangles()
    {
        Mesh mesh = new Mesh();
        GetComponent<MeshFilter>().mesh = mesh;

        mesh.vertices = vertices.ToArray();
        mesh.triangles = triangles.ToArray();
        mesh.RecalculateNormals();

        if (!faceNormalsOnOutside)
        {
            mesh.triangles = mesh.triangles.Reverse().ToArray();
        }
    }

    void TriangulateCube(Cube cube)
    {
        for (int i = 0; triTable[cube.configuration, i] != -1; i += 3)
        {
            Node[] points = new Node[3];

            Debug.Log(cube.configuration);
            Debug.Log(i);

            points[0] = cube.edgeNodes[triTable[cube.configuration, i    ]];
            points[1] = cube.edgeNodes[triTable[cube.configuration, i + 1]];
            points[2] = cube.edgeNodes[triTable[cube.configuration, i + 2]];

            AssignVertices(points);
        }
    }

    void AssignVertices(Node[] points)
    {
        for (int i = 0; i < points.Length; i++)
        {
            if(points[i].vertexIndex == - 1)
            {
                points[i].vertexIndex = vertices.Count;
                vertices.Add(points[i].position);
            }
        }

        AssignTriangles(points);
    }

    void AssignTriangles(Node[] points)
    {
        triangles.Add(points[0].vertexIndex);
        triangles.Add(points[1].vertexIndex);
        triangles.Add(points[2].vertexIndex);
    }

    public void ClearMesh()
    {
        cubeGrid = null;

        vertices.Clear();
        triangles.Clear();

        Mesh mesh = GetComponent<MeshFilter>().sharedMesh;
        
        mesh.Clear(false);

        mesh.vertices = vertices.ToArray();
        mesh.triangles = triangles.ToArray();
    }
